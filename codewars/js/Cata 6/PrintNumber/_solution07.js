function printNumber(n,c){
  const codex = [
    [' 1111 ','11  11','11  11','11  11','11  11',' 1111 '],
    ['  11  ',' 111  ','1 11  ','  11  ','  11  ','111111'],
    [' 1111 ','11  11','   11 ','  11  ',' 11   ','111111'],
    [' 1111 ','11  11','   11 ','   11 ','11  11',' 1111 '],
    ['11  11','11  11','11  11',' 11111','    11','    11'],
    ['111111','11    ','11111 ','    11','    11','11111 '],
    ['   11 ','  11  ',' 1111 ','11  11','11  11',' 1111 '],
    ['111111','11  11','   11 ','  11  ',' 11   ',' 11   '],
    [' 1111 ','11  11',' 1111 ',' 1111 ','11  11',' 1111 '],
    [' 1111 ','11  11','11  11',' 1111 ','  11  ',' 11   ']
  ].map(e => e.map(v => v.replace(/1/g,c)));
  const [b1,b2] = [c,' '].map(e => e.repeat(38)),
      nr = [...n.toString().padStart(5,'0')].reduce((a,e) => (codex[+e].forEach((v,c) => a[c] += v + ' '),a),Array(6).fill('  ')).map(e => e + ' ');
  return [b1,b2,...nr,b2,b1].map(e => c + e + c).join('\n');
}